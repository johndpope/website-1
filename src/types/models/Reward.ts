
export interface Reward {

  /**
   * The name of the reward.
   */
  name: string

  /**
   * The reward slug.
   */
  slug: string

  /**
   * The reward details.
   */
  details: string

  /**
   * The reward image.
   */
  image: string

  /**
   * The reward promoter handle.
   */
  promoter: string

  /**
   * The reward promoter name.
   */
  promoterName: string

  /**
   * The reward terms and conditions.
   */
  termsAndConditions: string[]

}
